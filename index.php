        	<br>
            <h2 class="tittle">Acerca de <span>Luis</span> Robles</h2>
            <span class="tittle-line"></span>
            <p class="text-justify">
            Nacido el 10 de mayo del año 1972 en la ciudad de <strong>Monteros</strong>, Provincia de Tucuman, por razones laborales de 
            su padre, se radica en <strong>Buenos Aires</strong> a los 6 años de edad.
            </p> 
            
            <p class="text-justify">
            Pintor de formación autodidacta que desde temprana edad comenzó a desarrollar una técnica y estilo basados en su admiración
            por los grandes maestros contemporáneos de la pintura argentina tales como <strong>Jorge Rajadell</strong>,
            <strong>Vito Campanella</strong>, <strong>Juan Lascano</strong> y de grandes pintores universales como 
            <strong>Zurbaran</strong>, <strong>Velazques</strong>, entre otros.
            </p>
            
            <p class="text-justify">
            Concurre al taller del maestro <strong>Jorge Rajadell</strong> con quien desarrolla la técnica del pelaje de los 
            animales en acrílico sobre tela.
            </p>
            
            <p class="text-justify">
            Con el paso del tiempo, la notable atracción por los animales y la naturaleza lo llevo a cultivar el 
            <strong>Animal Art</strong> y la pintura de paisajes por encima de otros generos pictóricos más tradicionales.
            </p>
            
            <p class="text-justify">
            En relación a su obra, se puede apreciar en cada una de ellas el esfuerzo y la maestria con la que captura la 
            luz en el paisaje como la esencia del mundo animal.
            </p>
            
            <p class="text-justify">
            Su particular estilo detallista esta en continua evolución hasta el presente; de hecho, en la actualidad sus 
            obras reflejan una armonica combinacion entre la precisa representación del animal y la interpretación del 
            entorno, éste ultimo de un modo estrictamente detallista aportando un equilibrio en la composición de la obra.
            Al mismo tiempo, una gran variedad de elementos y conceptos de la pintura tradicional es desplegada, revelando 
            un absoluto control sobre la luz, espacio, color, volumen, composición, etc.
            </p>
            
            <p class="text-justify">
            Indudablemente, uno de los principales meritos en el arte de <strong>Luis Robles</strong> consiste la habilidad 
            a la hora de retratar a los <strong>grandes felinos</strong> es aquí donde se hace evidente su fascinación por 
            estas criaturas. En efecto, Luis es capaz de generar una sutil conexión entre el espectador y el animal que retrata,
            en cuya expresión se percibe una sorprendente y misteriosa afinidad con determinados sentimientos humanos.
            </p>    
            
            <p class="text-justify">
            Es preciso destacar y poner especial interés en sus pinturas de paisajes, ya que en ellas se pueden encontrar 
            claros ejemplos de su madurez artística.
            </p>        
                       
